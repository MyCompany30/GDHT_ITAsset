package com.gdht.itasset.pojo;

public class PlanDetail {
	private String id;     //id
	private String title;  //名称
	private String depts;  //部门（仓库）
	private String number;	   //数量
	private String detail; //备注
	private String qdtime; //启动日期
	private String wctime; //完成日期
	private String yp;     //已盘数量
	private String wp;	   //未盘数量
	private String pk;	   //盘亏数量
	private String py;     //盘盈数量
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDepts() {
		return depts;
	}
	public void setDepts(String depts) {
		this.depts = depts;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getQdtime() {
		return qdtime;
	}
	public void setQdtime(String qdtime) {
		this.qdtime = qdtime;
	}
	public String getWctime() {
		return wctime;
	}
	public void setWctime(String wctime) {
		this.wctime = wctime;
	}
	public String getYp() {
		return yp;
	}
	public void setYp(String yp) {
		this.yp = yp;
	}
	public String getWp() {
		return wp;
	}
	public void setWp(String wp) {
		this.wp = wp;
	}
	public String getPk() {
		return pk;
	}
	public void setPk(String pk) {
		this.pk = pk;
	}
	public String getPy() {
		return py;
	}
	public void setPy(String py) {
		this.py = py;
	}
	
	

}
